"""Regenerate docs/reference/automations.md from live Dagster definitions.

Reads schedules and sensors from every code location's Definitions object
and writes a structured Markdown catalog.

Prerequisites:
    uv sync
    uv run scripts/dbt-manifest.py  # prepare dbt manifests for all projects

Usage:
    uv run scripts/gen-automations-doc.py
"""

import logging
import warnings
from importlib import import_module
from pathlib import Path

from cron_descriptor import Options, get_description

logging.disable(logging.CRITICAL)
warnings.filterwarnings("ignore")

OUTPUT = Path("docs/reference/automations.md")

CODE_LOCATIONS = [
    ("kipptaf", "Network-wide code location covering CMO-level integrations."),
    ("kippnewark", ""),
    ("kippcamden", ""),
    ("kippmiami", ""),
    ("kipppaterson", ""),
]

_CRON_ALIASES = {
    "@hourly": "0 * * * *",
    "@daily": "0 0 * * *",
    "@midnight": "0 0 * * *",
    "@weekly": "0 0 * * 0",
    "@monthly": "0 0 1 * *",
    "@yearly": "0 0 1 1 *",
    "@annually": "0 0 1 1 *",
}

_DESCRIPTOR_OPTIONS = Options()
_DESCRIPTOR_OPTIONS.use_24hour_time_format = False


def _describe_cron(cron: str) -> str:
    expanded = _CRON_ALIASES.get(cron, cron)
    return get_description(expanded, _DESCRIPTOR_OPTIONS)


def _cron_display(cron: str | list[str]) -> str:
    if isinstance(cron, list):
        return "; ".join(_describe_cron(c) for c in cron)
    return _describe_cron(cron)


def _interval_display(seconds: int | None) -> str:
    if seconds is None:
        return "—"
    if seconds < 60:
        return f"{seconds} sec"
    return f"{seconds // 60} min"


def _asset_targets(jobs: list, asset_graph, code_location: str) -> str:
    all_keys: set = set()
    for job in jobs:
        selection = getattr(job, "selection", None)
        if selection is None:
            continue
        try:
            all_keys |= selection.resolve(asset_graph)
        except Exception:  # nosec B110
            pass

    if not all_keys:
        return "—"

    paths = sorted(
        "/".join(k.path[1:]) if k.path[0] == code_location else "/".join(k.path)
        for k in all_keys
    )
    if len(paths) > 5:
        shown = ", ".join(f"`{p}`" for p in paths[:5])
        return f"{shown} + {len(paths) - 5} more"
    return ", ".join(f"`{p}`" for p in paths)


def _row(*cells: str) -> str:
    return "| " + " | ".join(cells) + " |"


def generate() -> None:
    lines = [
        "# Automations Catalog",
        "",
        "All schedules and sensors across every Dagster code location.",
        "All times use the `America/New_York` timezone.",
        "",
        "<!-- Generated by scripts/gen-automations-doc.py — do not edit manually -->",
        "",
    ]

    for code_location, description in CODE_LOCATIONS:
        try:
            mod = import_module(f"teamster.code_locations.{code_location}.definitions")
            defs = mod.defs
        except Exception as e:
            print(f"⚠  Skipping {code_location}: {e}")
            continue

        schedules = sorted(defs.schedules or [], key=lambda s: s.name)
        sensors = [
            s
            for s in sorted(defs.sensors or [], key=lambda s: s.name)
            if not s.name.endswith("__automation_condition_sensor")
        ]

        asset_graph = defs.resolve_asset_graph()

        lines += [f"## {code_location}", ""]
        if description:
            lines += [description, ""]

        if schedules:
            lines += [
                "### Schedules",
                "",
                _row("Schedule", "Runs", "Assets", "Description"),
                _row("---", "---", "---", "---"),
            ]
            for sched in schedules:
                desc = getattr(sched, "description", None) or "—"
                assets = _asset_targets([sched.job], asset_graph, code_location)
                lines.append(
                    _row(
                        f"`{sched.name}`",
                        _cron_display(sched.cron_schedule),
                        assets,
                        desc,
                    )
                )
            lines.append("")

        if sensors:
            lines += [
                "### Sensors",
                "",
                _row("Sensor", "Min interval", "Assets", "Description"),
                _row("---", "---", "---", "---"),
            ]
            for sensor in sensors:
                interval = _interval_display(sensor.minimum_interval_seconds)
                desc = getattr(sensor, "description", None) or "—"
                try:
                    jobs = sensor.jobs
                except Exception:
                    jobs = []
                assets = _asset_targets(jobs, asset_graph, code_location)
                lines.append(_row(f"`{sensor.name}`", interval, assets, desc))
            lines.append("")

        lines += ["---", ""]

    # Remove trailing separator
    while lines and lines[-1] in ("---", ""):
        lines.pop()

    OUTPUT.write_text("\n".join(lines) + "\n")
    print(f"✓ Written {OUTPUT}")


if __name__ == "__main__":
    generate()
